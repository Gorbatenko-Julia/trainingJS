function testCycle(a, b) {
    let x = "";
    
    if (a >= b) { //условие если а больше или равно б
        for (;b <= a; b++){ //инициализация условие изменение
            x = x + b + " "; // тело цикла 
       }
    }
       return x;
    

     if (a <= b) { //условие если а меньше или равно б 
        for (;a <= b; a++) {//инициальзация условие изменение
           x = x + a + " "; // тело цикла 
       }
     }
    
   return x;
}